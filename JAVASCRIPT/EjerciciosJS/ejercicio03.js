// 3.1 Multiplica 10 por 5 y muestra el resultado mediante console.
let a = 10;
let b = 5;
let multi = a * b;
console.log("El resultado de multiplicar 10 por 5 es: " + multi);
// muestra: El resultado de multiplicar 10 por 5 es: 50

// 3.2 Divide 10 por 2 y muestra el resultado en un console.
let dividendo = 10;
let divisor = 2;
let division = dividendo / divisor;
console.log("El resultado de dividir 10 por 2 es: " + division);
// muestra: El resultado de dividir 10 por 2 es: 5

// 3.3 Muestra mediante un console el resto de dividir 15 por 9.
let num1 = 15;
let num2 = 9;
let resto = num1 % num2;
console.log("El resto de dividir 15 por 9 es: " + resto);
// muestra: El resto de dividir 15 por 9 es: 6

// 3.4 Usa el correcto operador de asignación que resultará en o = 15
// Teniendo dos variables p = 10 y j = 5.
let p = 10;
let j = 5;
let o = p + j; // Operador de asignación suma
console.log("El valor de o es: " + o);
// muestra: El valor de o es: 15
