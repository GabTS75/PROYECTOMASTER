const numbers = [12, 21, 38, 5, 45, 37, 6];
const edades = [23, 15, 18, 20, 10, 30, 50];

function average(numberList) {  // Completar
    let contador = 0;
    for (number of numberList) {
        contador += number;
    }
    console.log(contador);
    console.log(contador / numberList.length);
}

average(numbers); // Debería retornar 24.57
average(edades);

